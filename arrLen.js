// Create a function that takes an array as a parameter and returns its length.
function arrLen(arr) {
    return arr.length
}

const arr1 = [3, 5, 2, 5]
const arr2 = [3, 7, 8, 0, 5, 5, 6, 2, 56, 21]
const arr3 = ['Create', 'a', 'function', 'that', 'takes', 'an', 'array', 'as']

console.log(arrLen(arr1)) // Output : 4
console.log(arrLen(arr2)) // Output : 10
console.log(arrLen(arr3)) // Output : 8