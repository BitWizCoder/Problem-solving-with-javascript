// Create a function that takes a string as a parameter and returns its length.

function strLen(str) {
    return str.length;
}

const str1 = "Hello World!";
const str2 = "Javascript is Awesome.";
const str3 = "You are a an Awesome Programmer.";

console.log(strLen(str1)); // Output : 12
console.log(strLen(str2)); // Output : 22
console.log(strLen(str3)); // Output : 10
